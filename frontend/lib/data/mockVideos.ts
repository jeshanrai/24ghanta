export const getLatestVideos = (limit = 4): Video[] => {
  return [...mockVideos]
    .sort((a, b) => new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime())
    .slice(0, limit);
};

export const getShortStories = (limit = 10): Video[] => {
  return mockShortStories.slice(0, limit);
};

export const getInstagramReels = (limit = 5): Video[] => {
  return mockShortStories
    .filter((v) => v.type === 'instagram')
    .slice(0, limit);
};
