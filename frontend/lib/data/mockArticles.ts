export const getFeaturedArticle = (): Article => {
  return mockArticles.find((a) => a.isFeatured) || mockArticles[0];
};

export const getRelatedArticles = (articleId: string, limit = 3): Article[] => {
  return mockArticles.filter((a) => a.id !== articleId).slice(0, limit);
};

export const getArticlesByCategory = (categorySlug: string, limit = 5): Article[] => {
  return mockArticles
    .filter((a) => a.category.slug === categorySlug)
    .slice(0, limit);
};

export const getLatestArticles = (limit = 10): Article[] => {
  return [...mockArticles]
    .sort((a, b) => new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime())
    .slice(0, limit);
};

export const getArticleBySlug = (slug: string): Article | undefined => {
  return mockArticles.find((a) => a.slug === slug);
};

export const getAllArticleSlugs = (): string[] => {
  return mockArticles.map((a) => a.slug);
};
